export function findStoreByCode(offCode: string): AladinStore | undefined {
  return ALADIN_STORES.find(
    (store) => store.offCode.toLowerCase() === offCode.toLowerCase()
  );
}

/**
 * 지역별 매장 목록 가져오기
 */
export function getStoresByRegion(region: string): AladinStore[] {
  return ALADIN_STORES.filter((store) => store.region === region);
}

/**
 * 전체 매장 수
 */
export const TOTAL_STORE_COUNT = ALADIN_STORES.length;
